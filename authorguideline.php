						  <h3 style=" font-size: 20px; color: #000; line-height:35px; "> ARTICLE TYPES AND WORD LIMITS</h3>
						<p style="Text-align: justify;   font-size: 13px; line-height: 20px;">
						JFMG is a leading and peer-reviewed scholarly journal in the broad field of finance and governance. It aims to publish original contributions that add to the literature in finance, financial markets, and governance-related issues. Please see the detail scope at: <a href="http://jfmg.bicm.ac.bd/aboutjfmg.php">http://jfmg.bicm.ac.bd/aboutjfmg.php</a>.
							 <br/><br/>
							Contributions are sought under the following five categories and world limits apply accordingly.
							<br/>
							<ul style="Text-align: justify;  line-height: 20px; font-size: 13px;">
							      <li><b>Original articles</b>  –  Empirical and theoretical original research article that address research questions using rigorous methods and produce findings that have significant contribution to the financial services literature and the industry. Original articles should generally not exceed 10000 words excluding tables, references, and appendices.<p></p></li>
							    
							      <li><b>Industry notes</b> – Analytical insights on market and economic issues from relevant industry experts from across sub-sectors such as commercial banking, investment banking, brokers, asset managers, and so on. These pieces are expected to create a shared development of knowledge bridging between academia and industry. Industry notes should generally not exceed 5000 words excluding tables, references, and appendices.<p></p></li>
							     
							      <li><b>Review article</b> – Articles produced based on the review of existing literature that not only summarize the existing findings and discussions but also provide valuable future research direction. Reviewed article should generally not exceed 8000 words excluding tables, references, and appendices.<p></p></li>
							     
							      <li><b>Book reviews</b> – Reviews of well-known, preferably recent, books addressing any of the key scope areas of journal that are published by world’s renowned publishers. These pieces are expected to be critical in discussion evaluating the published works of other scholars and identifying the future areas of research. Book reviews should generally not exceed 4000 words excluding tables, references, and appendices.<p></p></li>
							     
							      <li><b>Invited Articles/Letters/Notes</b> – Articles or industry notes or letters in nature are exclusively invited by the journal from renowned experts in relevant fields. No world limit applies to the Invited.
</li>
							</ul>
						</p>
						<br/>
						  <h3 style=" font-size: 20px; color: #000; line-height:35px; ">MANUSCRIPT REQUIREMENTS</h3>
						<p style="Text-align: justify;   font-size: 13px; line-height: 20px;">
                             JFMG has adopted the ‘Your Paper Your Way’ policy in order to facilitate hassle-free and easy submissions of scholarly works. It means there is no mandatory requirement of applying any specific formatting style and supplying figures separately. <br/><br/>
                               During the first submission, authors need to submit their manuscripts as single MS Word or PDF files, including all figures, figure legends, and references. Of course, all papers need to include the generic elements that are applicable to the particular article type; such as title, abstract, introduction, materials & methods, results, discussion (or results and discussion combined), conclusion, references, and figures.  You can use any referencing style for initial submissions.<br/><br/>
                               Only when your paper is at the revision stage and you are asked to submit a revision in response to reviews, you will be requested to transform your paper into the 'correct format' for acceptance and provide the items necessary for your article’s final publication.
                                <br/><br/>
                                <b>Initial Submissions </b><br/>
                                Upload your manuscript files all-inclusive of title page, main document, references, figures, and tables. Supplementary files can be uploaded separately. Documents should be prepared in MS Word or Latex and should be submitted in MS Word or PDF version. <br/><br/>
                               In the submission system, for reviewing purposes, you should upload a single .pdf for the main manuscript that you have generated from your source files. You must use the File Designation "Main Document" from the dropdown box. You should also submit a Title Page separately which will contain Author name, affiliation and contact details. You can then upload the supplementary files using the “Supplementary Files” File Designation from the dropdown box.  <br/><br/>
                                <b>Revised Submissions </b><br/>
                                Original and Editable source files must be uploaded at this stage. Tables must be on separate pages after the reference list, and not be incorporated into the main text. Figures should be uploaded as separate figure files in JPEG or TIFF format.   <br/>
                               <br/> For Latex users, in addition you must upload your TeX source files. For all your source files you must use the File Designation "Supplemental Material not for review".  <br/><br/>
                                During revised submission, previous versions of uploaded documents must be deleted. If your manuscript is accepted for publication, we will use the files you upload to typeset your article within a totally digital workflow. <br/>



						
						</p>
						<br/>
						 <h3 style=" font-size: 20px; color: #000; line-height:35px; ">PREPARING YOUR SUBMISSION</h3>
						<p style="Text-align: justify;   font-size: 13px; line-height:20px;">
				
                            Parts of the Manuscript For first submission, the manuscript should be submitted in separate files: (1) title page and (2) main text file including figures, tables, and references. The first title page should contain: the article title, author name, affiliation, contact details, any and acknowledgements pertaining to the work. The main document should contain title, abstract, keywords, JEL classifications, acknowledgement, declarations, and the main content of the article. The main content generally should include Introduction, Literature Review, Methodology, Analysis/Discussion or both together, references, and appendices. Supplementary files can be submitted separately in the submission system.<br/><br/>
                            Upload both as separate documents, under file designation ‘Title Page’ and ‘Main Document’. The present address of any author, if different from where the work was carried out, should be supplied in a footnote. <br/><br/>
                            For Revised Submissions, Title Page, Main Document, any editable Source Files (e.g., Figures, TeX source files), and References must be submitted in American Psychological Association (APA) style so that we can fast process your work for publication.
                            						
                            						
						
						</p>
						<br/>
					<h3 style=" font-size: 20px; color: #000; line-height:35px; ">REFERENCING  STYLE</h3>
						
						<p style="Text-align: justify;   font-size: 13px; line-height:20px;">
					All papers at the revision stage only are required to transform all in-text and end-of-the paper articles into the American Psychological Association (APA) referencing style. Consult <a href="https://apastyle.apa.org/">https://apastyle.apa.org/</a> for details.
						</p>
						<br/>
						<h5 style="Text-align: justify;   font-size: 15px;">AUTHORSHIP</h5>
							<p style="Text-align: justify;   font-size: 13px; line-height:20px;">
						Those who have truly and authentically participated in conducting the work should be given Authorship. Please be careful in listing all authors and author credit sequences (e.g., first and second authorship) appropriate on the Title Page while submitting your work. After the initial submission of a work, authorship or credit sequence cannot be changed.
						<br/>
                          <br/><b> Acknowledgments</b> <br/>
                            The Acknowledgement section may list all people who do not meet the criteria for authorship but have helped in the work. Thanks to anonymous reviewers are not appropriate. <br/><br/>
                            <b>Conflict of Interest Statement</b> <br/>
                            Authors will be asked to provide a conflict of interest statement during the submission process. All authors of a work must agree and approve to disclose any ethical and financial conflict of interests associated with the work. <br/><br/>
                            Conflict of Interest Statement should be included in the initial submission after the References section or on the Title page. If no conflict of interest is associated, all author must agree, approve and disclose that ‘No conflict of interest, either ethical or financial, is associate with this article’.
                            </P>
