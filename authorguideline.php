						  <h3 style=" font-size: 20px; color: #000; line-height:35px; ">USING THE SUBMISSION SYSTEM</h3>
						<p style="Text-align: justify;   font-size: 13px; line-height: 20px;">
				Please visit <a href="http://jfmg.bicm.ac.bd/login.php">http://jfmg.bicm.ac.bd/login.php</a> to login as an author or reviewer. If you are first time user, you must register yourself and open an account using the ‘Register as a New User’ option. Once your registration is completed, you can login and proceed with your submission or review.
